package indentifers;

public class Identificater$ {
    public static void main(String[] args) {
        String _$ = "chetos";
        String $_ = "Chanchito feliz " + _$;
        String $_3 = "Rodriguez";
        System.out.println("Valor $: "+ _$);
        System.out.println("Valor $_: "+ $_);
        System.out.println("Valor $_3: "+ $_3);
    }
}
